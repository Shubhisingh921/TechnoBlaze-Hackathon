package com.pride.item;

public class ItemSlider {
	
	private String SliderId;
    private String SliderType;
    private String SliderImage;
    private String SliderLink;
    private String SliderRId;

    public String getSliderId() {
        return SliderId;
    }

    public void setSliderId(String sliderId) {
        SliderId = sliderId;
    }

    public String getSliderType() {
        return SliderType;
    }

    public void setSliderType(String sliderType) {
        SliderType = sliderType;
    }

    public String getSliderImage() {
        return SliderImage;
    }

    public void setSliderImage(String sliderImage) {
        SliderImage = sliderImage;
    }

    public String getSliderLink() {
        return SliderLink;
    }

    public void setSliderLink(String sliderLink) {
        SliderLink = sliderLink;
    }

    public String getSliderRId() {
        return SliderRId;
    }

    public void setSliderRId(String sliderRId) {
        SliderRId = sliderRId;
    }


}
